
// double equation takes the first value, converts the second value type to first value type then matches both values

const taka = 10;
const poisha = "10";
if (taka == poisha){
    console.log("it is true");
}
else{
    console.log("it is false");
}


///triple equation takes the first value, checks the second value type. if both types mismatch it results false
const num1 = 10;
const num2 = "10";
if (num1 === num2){
    console.log("it is true");
}
else{
    console.log("it is false");
}